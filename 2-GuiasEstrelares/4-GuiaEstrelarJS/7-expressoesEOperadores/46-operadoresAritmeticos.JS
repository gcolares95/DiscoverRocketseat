// Operadores Aritméticos

// multiplicação *
console.log(3.2 * 5);
// divisão /
console.log(10 / 2);
// soma +
console.log(5 + 3.33);
// subtração -
console.log(34 - 23);
// resto da divisão %
console.log(9 % 2);
// incremento ++
let increment = 0;
console.log(++increment);
// decremento --
let decrement = 3;
console.log(--decrement);
// exponencial **
console.log(5 ** 3);